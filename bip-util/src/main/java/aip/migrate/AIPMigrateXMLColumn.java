package aip.migrate;

public class AIPMigrateXMLColumn {
	String column_src;
	String column_dest;
	boolean islob;
	
	public String getColumn_src() {
		return column_src;
	}
	public void setColumn_src(String column_src) {
		this.column_src = column_src;
	}
	public String getColumn_dest() {
		return column_dest;
	}
	public void setColumn_dest(String column_dest) {
		this.column_dest = column_dest;
	}
	public boolean isIslob() {
		return islob;
	}
	public void setIslob(boolean islob) {
		this.islob = islob;
	}
	

}
